public class Unary {

    public static void main(String[] args) {
        int number = 10;
        System.out.println(number); // 10
        System.out.println(number--); // 10 print but 10-1 --> 9
        System.out.println(number); // 9 save in memory
        System.out.println(--number); // 8
    }
}
